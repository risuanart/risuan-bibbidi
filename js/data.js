// 韓文組字：把子音(초성)、母音(중성)、收尾子音(종성，可省略)組成一個完整的韓文字方塊
const HANGUL_CHO_MAP = { 'ㄱ':0,'ㄴ':2,'ㄷ':3,'ㄹ':5,'ㅁ':6,'ㅂ':7,'ㅅ':9,'ㅇ':11,'ㅈ':12,'ㅊ':14,'ㅋ':15,'ㅌ':16,'ㅍ':17,'ㅎ':18 };
const HANGUL_JUNG_MAP = { 'ㅏ':0,'ㅑ':2,'ㅓ':4,'ㅕ':6,'ㅗ':8,'ㅛ':12,'ㅜ':13,'ㅠ':17,'ㅡ':18,'ㅣ':20 };
const HANGUL_JONG_MAP = { '':0,'ㄱ':1,'ㄴ':4,'ㄷ':7,'ㄹ':8,'ㅁ':16,'ㅂ':17,'ㅅ':19,'ㅇ':21,'ㅈ':22,'ㅊ':23,'ㅋ':24,'ㅌ':25,'ㅍ':26,'ㅎ':27 };

function composeHangul(cho, jung, jong){
  const choIdx = HANGUL_CHO_MAP[cho];
  const jungIdx = HANGUL_JUNG_MAP[jung];
  const jongIdx = HANGUL_JONG_MAP[jong ?? ''];
  if(choIdx === undefined || jungIdx === undefined || jongIdx === undefined) return null;
  const code = 0xAC00 + (choIdx * 21 + jungIdx) * 28 + jongIdx;
  return String.fromCharCode(code);
}

