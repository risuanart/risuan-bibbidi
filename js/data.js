// 把方格表轉成 SVG 字串，cell 是每個像素在畫面上的大小(px)，color 是填色
function svgFromPattern(pattern, opts={}){
  const cell = opts.cell || 3;
  const color = opts.color || "#f3f1ea";
  const rows = pattern.length, cols = pattern[0].length;
  let rects = "";
  for(let r=0;r<rows;r++){
    for(let c=0;c<cols;c++){
      const v = pattern[r][c];
      if(v === "1" || v === 1){
        rects += `<rect x="${c*cell}" y="${r*cell}" width="${cell}" height="${cell}" fill="${color}"/>`;
      }
    }
  }
  return `<svg width="${cols*cell}" height="${rows*cell}" viewBox="0 0 ${cols*cell} ${rows*cell}" xmlns="http://www.w3.org/2000/svg">${rects}</svg>`;
}

